this_list = [10, 7, 3, 8, 9, 1, 2]
print("Original list: ", this_list)

# add an element to the list
this_list.append(5)
print("List after adding an element: ", this_list)

# remove an element from the list
this_list.remove(8)
print("List after removing an element: ", this_list)

# sort the list
this_list.sort()
print("Sorted list: ", this_list)
